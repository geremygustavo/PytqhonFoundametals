# Fundamental Data Types
# Int
a = 5
print("Type of a: ", type(a))
# Float
b = 5.0
print("Type of b: ", type(b))

# Str
string_value = "I'm a Abel"
print("type of string_value: ", type(string_value))

# Booleam
print(type(True))
print(type(False))

# List
names_list = [['abel', 'test'], ['lists']]
print("Type of names_list: ", type(names_list))

# Tuple
tuple_a = (0, 1, 2, 3)
tuple_b = ('python', 'test')
tuple_c = (tuple_a, tuple_b)
print("type of Tuple3: ", type(tuple_c))

# Set
set_a = {1, 2, 'Geeks', 4, 'For', 6, 'Geeks'}
print("Type of set1: ", type(set_a))

# Dict
dict_a = {1: 'Geeks', 'name': 'For', 3: 'Geeks'}
print("Type of dict_a: ", type(dict_a))
